    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String id = request.getParameter("id");
        System.out.println("id" + id);
        ProfileDAO fDAO = new ProfileDAO();
        DAO dao = new DAO();

        Profile profile = fDAO.getProfileByUserId(Integer.parseInt(id));
        System.out.println("profile.getStatus() " + profile.getStatus());
        System.out.println(profile.toString());
        if (profile != null) {
            if (profile.getStatus().equals("active")) {
                dao.updateAccountStatus(id, "inactive");
            }
            if (profile.getStatus().equals("inactive")) {
                dao.updateAccountStatus(id, "active");
            }

            //   boolean accountUpdated = dao.updateAccountStatus(id, newStatus);
        }
        String referer = request.getHeader("referer");
        response.sendRedirect(referer);

    }

