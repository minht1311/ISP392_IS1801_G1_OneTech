    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String code = request.getParameter("authcode");
        HttpSession session = request.getSession();
        Account a = (Account) session.getAttribute("authcode");

        if (code.equalsIgnoreCase(a.getCode())) {
            // Code verified successfully
            response.sendRedirect("resetPW.jsp");
        } else {
            // Verification failed
            request.setAttribute("error", "Invalid verification code. Please try again.");
            request.getRequestDispatcher("emailverificationFP.jsp").forward(request, response);
        }
    }
